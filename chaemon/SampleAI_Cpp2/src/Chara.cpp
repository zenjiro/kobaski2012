#include "Chara.h"

void Chara::initializeChara(Chara::CharaType type, int score, int x, int y, Chara::Direction direction, Chara::CharaState state, int stateRemainingTime) {
	this->type = type;
	this->score = score;
	this->x = x;
	this->y = y;
	this->direction = direction;
	this->state = state;
	this->stateRemainingTime = stateRemainingTime;
}

Chara::CharaType Chara::getType() {
	return type;
}


Chara::CharaState Chara::getState() {
	return state;
}

int Chara::getScore() {
	return score;
}

int Chara::getX() {
	return x;
}

int Chara::getY() {
	return y;
}

int Chara::getStateRemainingTime() {
	return stateRemainingTime;
}

Chara::Direction Chara::getDirection() {
	return direction;
}

